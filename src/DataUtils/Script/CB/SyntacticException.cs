    public class CBException : Exception
    {
        public object Value { get; set; }
        public CBException(object value)
        {
            Value = value;
        }
    }

    public class SyntacticException : Exception
    {
        string Msg = string.Empty;
        public Token Token { get; set; }
        public SyntacticException(Token token, string msg, int erorcode)
        {
            Token = token;
            if (token != null)
            {
                Msg = "errorcode:" + erorcode + msg + ";" + token.GetPosition();
            }
            else
            {
                Msg = "errorcode:" + erorcode + msg;
            }
        }
        public void SetMsg(string msg)
        {
            Msg = msg;
        }
        public override string Message { get { return Msg; } }
    }
